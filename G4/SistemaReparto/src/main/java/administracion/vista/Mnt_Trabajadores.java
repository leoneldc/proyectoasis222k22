    private void Btn_modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_modificarMouseClicked
        if (procesosr.isEmptyTxf(Txt_id, Txt_nombre, Txt_nombrePT, Txt_apellido, Txt_user, Txt_password, Txt_email)) {
            if (procesosr.isNumeric(Txt_id)) {
                if (procesosr.isSelectedRbt(Rb_activo, Rb_inactivo)) {
                    if (procesosr.ValidarEmail(Txt_email.getText())) {
                        trabajadores.setIdTrabaajdor(Txt_id.getText());
                        trabajadores.setIdPuestoT(Txt_idPT.getText());
                        trabajadores.setNombre(Txt_nombre.getText());
                        trabajadores.setApellido(Txt_apellido.getText());
                        trabajadores.setUsuario(Txt_user.getText());
                        String passwordEncry = Hash.getMD5(Txt_password.getText());
                        trabajadores.setPassword(passwordEncry);
                        trabajadores.setEmail(Txt_email.getText());
                        if (Rb_activo.isSelected()) {
                            trabajadores.setEstado("1");
                        }
                        if (Rb_inactivo.isSelected()) {
                            trabajadores.setEstado("0");
                        }
                        trabajadoresDAO.update(trabajadores);
                        funcBitacora.GuardarBitacora("MODIFICAR", idApp);
                        cargarDatos("");
                        procesosr.accionExitosa("Modificación Exitosa", "Se ha modificado el trabajador: \"" + Txt_nombre.getText() + " " + Txt_apellido.getText() + "\" correctamente");
                        limpiar();
                    }
                }
            }
        }
    }//GEN-LAST:event_Btn_modificarMouseClicked

