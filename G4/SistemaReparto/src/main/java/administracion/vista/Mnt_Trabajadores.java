    private void Btn_listadoPTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_listadoPTActionPerformed
        if (procesosr.isNumeric(Txt_idPT)) {
            PuestosTrabajo puestoT = new PuestosTrabajo();
            PuestosTrabajoDAO puestoTDAO = new PuestosTrabajoDAO();
            puestoT.setId(Txt_idPT.getText());
            puestoT = puestoTDAO.selectV(puestoT);
            if (puestoT.getNombre() == null) {
                JOptionPane.showMessageDialog(null, "PUESTO DE TRABAJO NO EXISTE");
            } else {
                if (puestoT.getEstado().equals("1")) {
                    Txt_nombrePT.setText(puestoT.getNombre());
                } else {
                    JOptionPane.showMessageDialog(null, "PUESTO DE TRABAJO DESHABILITADO");
                }
            }
        }
    }//GEN-LAST:event_Btn_listadoPTActionPerformed

